<script setup>
import {ref, reactive, onMounted, computed} from 'vue'
import { useRouter, useRoute } from 'vue-router'
import TinyMCEEditor from '../../../components/TinyMCEEditor.vue'
import DatePicker from 'vue3-persian-datetime-picker'
import {useDanimPageStore} from "@/stores/danim-page.ts";
const danimPageStore=useDanimPageStore()
const router = useRouter()
const route = useRoute()

const isLoading = ref(false)
const loading = ref(true)
const pageFound = ref(false)
const isDragging = ref(false)
const notifications = ref([])
const autoGeneratedExcerpt = ref(false)
const showStatusDropdown = ref(false)
const showTemplateDropdown = ref(false)
const pageId = ref(null)

const form = reactive({
  title: '',
  slug: '',
  excerpt: '',
  content: '',
  status: 'draft',
  template: 'default',
  publishDate: null,
  image: null,
  metaTitle: '',
  metaDescription: '',
  ogTitle: '',
  ogDescription: '',
  ogImage: '',
  createdAt: '',
  updatedAt: '',
  views: 0
})
const pages = computed(()=>danimPageStore.pages)
const errors = reactive({
  title: '',
  excerpt: '',
  content: ''
})

const statuses = [
  { value: 'draft', label: 'پیش‌نویس', description: 'ذخیره بدون انتشار', icon: 'ti-file-text' },
  { value: 'published', label: 'منتشر شده', description: 'نمایش عمومی', icon: 'ti-check-circle' },
  { value: 'scheduled', label: 'زمان‌بندی شده', description: 'انتشار در تاریخ مشخص', icon: 'ti-clock' }
]

const templates = [
  { value: 'default', label: 'پیش‌فرض', description: 'قالب استاندارد با سایدبار' },
  { value: 'full-width', label: 'تمام عرض', description: 'بدون سایدبار، تمام پهنا' },
  { value: 'landing', label: 'لندینگ پیج', description: 'برای صفحات فرود' },
  { value: 'contact', label: 'تماس با ما', description: 'با فرم تماس' }
]

// Load page data
const loadPage = () => {
  pageId.value = route.params.id
  const page = pages.value.find(p => p.id === pageId.value)
  
  if (page) {
    Object.assign(form, page)
    pageFound.value = true
  } else {
    pageFound.value = false
  }
  
  loading.value = false
}

// Status Management
const getStatusLabel = (value) => {
  return statuses.find(s => s.value === value)?.label || 'پیش‌نویس'
}

const getStatusIcon = (value) => {
  return statuses.find(s => s.value === value)?.icon || 'ti-file-text'
}

const selectStatus = (value) => {
  form.status = value
  showStatusDropdown.value = false
}

// Template Management
const getTemplateLabel = (value) => {
  return templates.find(t => t.value === value)?.label || 'پیش‌فرض'
}

const selectTemplate = (value) => {
  form.template = value
  showTemplateDropdown.value = false
}

// Slug Management
const generateSlug = () => {
  if (form.title) {
    form.slug = form.title
      .toLowerCase()
      .trim()
      .replace(/[\s]+/g, '-')
      .replace(/[^\u0600-\u06FFa-z0-9\-]/g, '')
      .replace(/\-+/g, '-')
  }
}

const validateSlug = () => {
  form.slug = form.slug
    .toLowerCase()
    .trim()
    .replace(/[\s]+/g, '-')
    .replace(/[^\u0600-\u06FFa-z0-9\-]/g, '')
    .replace(/\-+/g, '-')
}

// Excerpt Management
const generateExcerptFromContent = () => {
  if (!form.content || form.content.trim() === '') {
    showNotification('لطفا ابتدا محتوا را وارد کنید', 'error')
    return
  }
  
  const tempDiv = document.createElement('div')
  tempDiv.innerHTML = form.content
  const text = tempDiv.textContent || tempDiv.innerText || ''
  const cleanText = text.trim()
  
  if (!cleanText) {
    showNotification('محتوای قابل استخراج یافت نشد', 'error')
    return
  }
  
  form.excerpt = cleanText.substring(0, 200).trim()
  
  if (form.excerpt) {
    autoGeneratedExcerpt.value = true
    showNotification('خلاصه با موفقیت تولید شد', 'success')
    setTimeout(() => {
      autoGeneratedExcerpt.value = false
    }, 3000)
  }
}

// Content Length
const getContentLength = () => {
  if (!form.content) return 0
  const tempDiv = document.createElement('div')
  tempDiv.innerHTML = form.content
  const text = tempDiv.textContent || tempDiv.innerText || ''
  return text.length
}

// Image Management
const removeImage = () => {
  form.image = null
}

const handleDrop = (e) => {
  e.preventDefault()
  isDragging.value = false
  
  const files = e.dataTransfer?.files
  if (files && files.length > 0) {
    handleImageUpload(files[0])
  }
}

const handleFileSelect = (e) => {
  const files = e.target?.files
  if (files && files.length > 0) {
    handleImageUpload(files[0])
  }
}

const handleImageUpload = (file) => {
  if (file && file.type.startsWith('image/')) {
    const reader = new FileReader()
    reader.onload = (e) => {
      form.image = e.target?.result
      showNotification('تصویر با موفقیت آپلود شد', 'success')
    }
    reader.readAsDataURL(file)
  } else {
    showNotification('لطفا یک فایل تصویری انتخاب کنید', 'error')
  }
}

// Form Validation
const validateForm = () => {
  let isValid = true
  
  errors.title = ''
  errors.excerpt = ''
  errors.content = ''
  
  if (!form.title.trim()) {
    errors.title = 'عنوان صفحه الزامی است'
    isValid = false
  }
  
  if (!form.excerpt.trim()) {
    errors.excerpt = 'خلاصه صفحه الزامی است'
    isValid = false
  }
  
  if (!form.content.trim()) {
    errors.content = 'محتوای صفحه الزامی است'
    isValid = false
  }
  
  return isValid
}

// Save Functions
// Save Draft
const saveDraft = async () => {
  if (!form.title.trim()) {
    showNotification('لطفا عنوان صفحه را وارد کنید', 'error');
    return;
  }

  isLoading.value = true;
  form.status = 'draft';
  form.updatedAt = new Date().toISOString();

  try {
    if (pageId.value) {
      // بروزرسانی موجود
      await danimPageStore.updatePage(pageId.value, { ...form });
    } else {
      // اضافه کردن صفحه جدید
      const newPage = await danimPageStore.addPage({ ...form });
      pageId.value = newPage.id;
    }

    showNotification('صفحه به عنوان پیش‌نویس ذخیره شد', 'success');

    setTimeout(() => {
      router.push('/dashboard/danim/pages');
    }, 1500);
  } catch (error) {
    console.error('❌ خطا در ذخیره پیش‌نویس:', error);
    showNotification('خطا در ذخیره پیش‌نویس!', 'error');
  } finally {
    isLoading.value = false;
  }
}

// Update Page
const updatePage = async () => {
  if (!validateForm()) {
    showNotification('لطفا فیلدهای الزامی را پر کنید', 'error');
    return;
  }

  isLoading.value = true;
  form.updatedAt = new Date().toISOString();

  try {
    await danimPageStore.updatePage(pageId.value, { ...form });

    showNotification('صفحه با موفقیت به‌روزرسانی شد! ✨', 'success');

    setTimeout(() => {
      router.push('/dashboard/danim/pages');
    }, 1500);
  } catch (error) {
    console.error('❌ خطا در بروزرسانی صفحه:', error);
    showNotification('خطا در به‌روزرسانی صفحه!', 'error');
  } finally {
    isLoading.value = false;
  }
}

